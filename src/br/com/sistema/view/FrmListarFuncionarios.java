    private void botaoEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoEditarMouseClicked

        if(botaoEditar.isEnabled())
        {
            FrmCadastrarFuncionarios telaCadastrarFuncionarios = new FrmCadastrarFuncionarios();

            telaCadastrarFuncionarios.nomeTela.setText("Editar Cadastro");
            telaCadastrarFuncionarios.setVisible(true);
            telaCadastrarFuncionarios.preencheCampos(tabelaFuncionarios);
            telaCadastrarFuncionarios.isRegister = false;
        }
    }//GEN-LAST:event_botaoEditarMouseClicked
