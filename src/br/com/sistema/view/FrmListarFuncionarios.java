    private void listarNaTabela(List<Funcionarios> lista)
    {
        DefaultTableModel dados = (DefaultTableModel) tabelaFuncionarios.getModel();
        dados.setNumRows(0);
        
        for(Funcionarios f : lista)
        {
            dados.addRow(new Object[]
            {
                f.getId(),
                f.getNome(),
                f.getRg(),
                f.getCpf(),
                f.getEmail(),
                f.getTelefone(),
                f.getCelular(),
                f.getCep(),
                f.getEndereco(),
                f.getNumero(),
                f.getComplemento(),
                f.getBairro(),
                f.getCidade(),
                f.getUf(),
                f.getNivelAcesso(),
                f.getCargo(),
                f.getSenha()
            });
        }
    }
