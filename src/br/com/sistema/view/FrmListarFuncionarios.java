    private void botaoExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoExcluirMouseClicked
       
        int op;
        Object[] options = { "Sim", "Não" };
        
        if(botaoExcluir.isEnabled())
        {
            op = JOptionPane.showOptionDialog(null, "Tem certeza que deseja excluir funcionário?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
            if(op == 0)
            {
                FuncionariosDAO dao = new FuncionariosDAO();
                dao.excluirFuncionario(Integer.parseInt(tabelaFuncionarios.getValueAt(tabelaFuncionarios.getSelectedRow(), 0).toString()));
            }
        }
    }//GEN-LAST:event_botaoExcluirMouseClicked
    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        
        FuncionariosDAO dao = new FuncionariosDAO();
        listarNaTabela(dao.listarFuncionarios());
        ativaDesativaBotoes(false);
    }//GEN-LAST:event_formWindowActivated

    private void tabelaFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaFuncionariosMouseClicked
        
        if(tabelaFuncionarios.isRowSelected(tabelaFuncionarios.getSelectedRow()))
        {
            ativaDesativaBotoes(true);
        }
    }//GEN-LAST:event_tabelaFuncionariosMouseClicked
