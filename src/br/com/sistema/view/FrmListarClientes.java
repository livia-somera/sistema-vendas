    private void botaoEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoEditarMouseClicked
        
       if(botaoEditar.isEnabled())
       {
           FrmCadastrarClientes telaCadastrarClientes = new FrmCadastrarClientes();
        
           telaCadastrarClientes.nomeTela.setText("Editar Cadastro");
           telaCadastrarClientes.setVisible(true);
           telaCadastrarClientes.preencheCampos(tabelaClientes);
           telaCadastrarClientes.isRegister = false;
       }       
    }//GEN-LAST:event_botaoEditarMouseClicked
