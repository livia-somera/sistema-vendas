    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        int qtd_estoque, qtd_comprada;
        
        Produtos objp = new Produtos();
        ProdutosDAO dao_produto = new ProdutosDAO();
        
        objp.setId(Integer.parseInt(txtcodigo.getText()));
        qtd_estoque = dao_produto.retornaEstoqueAtual(objp.getId());
        qtd_comprada = Integer.parseInt(txtqtd.getText());
        
        if(qtd_estoque >= qtd_comprada){
            // botao add item
            qtd = Integer.parseInt(txtqtd.getText());
            preco = Double.parseDouble(txtpreco.getText());
        
            subtotal = qtd * preco;
        
            total += subtotal;
            txttotal.setText(String.valueOf(total));
        
            //Adicionar o produto no carrinho        
            carrinho = (DefaultTableModel) tabelaItens.getModel();
        
            carrinho.addRow(new Object[]{
                txtcodigo.getText(),
                txtdescricao.getText(),
                txtqtd.getText(),
                txtpreco.getText(),
                subtotal           
            });
        }else{
            JOptionPane.showMessageDialog(null, "Quantidade indisponível no estoque. (Disponível para venda: " + qtd_estoque + " unidades)");
        }

    }//GEN-LAST:event_btnaddActionPerformed

