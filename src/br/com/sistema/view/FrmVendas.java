    private void adicionardescontoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionardescontoActionPerformed
        
        double totalVenda = Double.parseDouble(txttotal.getText());
        double totalDesconto = Double.parseDouble(txtdesconto.getText());
        
        if(totalDesconto < totalVenda)
        {
            double desconto = totalVenda - totalDesconto;
            String desc = String.valueOf(desconto);
            txttotalcomdesconto.setText(desc);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Valor inválido!", "ATENÇÃO", JOptionPane.ERROR_MESSAGE);
        }    
    }//GEN-LAST:event_adicionardescontoActionPerformed

